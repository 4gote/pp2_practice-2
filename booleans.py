is_sunny = True
is_raining = False

print("Is it sunny?", is_sunny)
print("Is it raining?", is_raining)

# Boolean operations
print("NOT sunny:", not is_sunny)
print("Sunny AND raining:", is_sunny and is_raining)
print("Sunny OR raining:", is_sunny or is_raining)

# Comparison results are booleans
temperature = 25
print("Is temperature > 20?", temperature > 20)
print("Is temperature == 25?", temperature == 25)
